  def searcher(at: String, currentTime: Int, record: Int, collectedItems: Seq[String], distances: scala.collection.mutable.Map[(String, String), Int], returnToStart: Boolean): Int = {
    if (collectedItems.length == 7) {
      val totalTime = currentTime + (if (returnToStart) distances(("0", at)) else 0)
      println(s"new record: $totalTime")
      return totalTime
    }
    val d = distances
      .filter(ssi => ssi._1._1 == at)
      .filterNot(ssi => collectedItems.contains(ssi._1._2))
      .toSeq
      .sortBy(ssi => ssi._2)
    var newrecord = record
    d.foreach { ssi =>
      val newTime = currentTime + ssi._2
      val astar = if (returnToStart) distances(("0", ssi._1._2)) else 0
      if (newTime + astar < newrecord) {
        val res = searcher(ssi._1._2, newTime, newrecord, collectedItems ++ Seq(ssi._1._2), distances, returnToStart)
        if (res < newrecord)
          newrecord = res
      }
    }
    newrecord
  }

