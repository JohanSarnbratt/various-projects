    steps(current, stepsTaken)
      .foreach { xy =>
        visited.put(xy, stepsTaken + 1)
        stepper(
          xy,
          stepsTaken + 1
        )
      }

  }

  def steps(current: (Int, Int), stepsTaken: Int): Seq[(Int, Int)] = {
    Seq((current._1 - 1, current._2), (current._1 + 1, current._2), (current._1, current._2 + 1), (current._1, current._2 - 1))
      .filter(isTrack)
      .filter(xy =>
        visited.getOrElse(xy, stepsTaken + 20) > stepsTaken + 1
      )
  }

